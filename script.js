const apiKey = "YOUR_API_KEY"; // Replace with your actual API key
const city = "Seoul";
const weatherUrl = `https://api.openweathermap.org/data/2.5/weather?q=${city}&appid=${apiKey}&units=metric`;

fetch(weatherUrl)
    .then(response => {
        if (!response.ok) {
            throw new Error("Weather data not available.");
        }
        return response.json();
    })
    .then(data => {
        const temperature = data.main.temp;
        const weatherDescription = data.weather[0].description;
        const weatherInfo = `Temperature: ${temperature}°C, Condition: ${weatherDescription.charAt(0).toUpperCase() + weatherDescription.slice(1)}`;
        document.getElementById("weather-info").innerText = weatherInfo;
    })
    .catch(error => {
        document.getElementById("weather-info").innerText = "Error fetching weather data.";
        console.error("Error fetching weather data:", error);
    });
